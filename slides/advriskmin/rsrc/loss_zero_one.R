# ------------------------------------------------------------------------------
# FIG: 0-1 LOSS
# ------------------------------------------------------------------------------

# DATA -------------------------------------------------------------------------

x <- seq(-2L, 2L, by = 0.01)
y <- as.numeric(x < 0L)

# PLOTS ------------------------------------------------------------------------

p <- ggplot2::qplot(
  x, 
  y, 
  geom = "line",
  xlab = expression(r = yf(x)),
  ylab = expression(L(yf(x))))

p <- p + theme_bw()
p <- p + theme(text = element_text(size = 20L))

ggplot2::ggsave("../figure/plot_loss_01.png", p)

